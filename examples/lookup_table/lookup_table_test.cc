TEST(LookupTableTest, TestDerivatives) {
  const Eigen::Vector3d a{0.53, -0.11, 0.32};
  Eigen::RowVector3d D1, D2;
  gen::lookup_method<double>(table, a, D1, D2);

  const auto D1_num = wf::numerical_jacobian(a, [](const Eigen::Vector3d& a) {
    return Eigen::Matrix<double, 1, 1>(gen::lookup_method<double>(table, a, nullptr, nullptr));
  });
  ASSERT_EIGEN_NEAR(D1_num, D1, 1.0e-5);

  // compute elementwise second derivatives
  for (std::size_t i = 0; i < 3; ++i) {
    const auto D2_num_i = wf::numerical_derivative(0.01, [&](double dx1) {
      return wf::numerical_derivative(0.001, [&](double dx2) {
        Eigen::Vector3d a_copy = a;
        a_copy[i] += dx1 + dx2;
        return gen::lookup_method<double>(table, a_copy, nullptr, nullptr);
      });
    });
    ASSERT_NEAR(D2_num_i, D2[i],
                0.02);  // TODO: really bad tolerance, check central difference math
  }
}

// Test the lookup table itself.
TEST(LookupTableTest, TestLookupTable) {
  for (double x : {-0.9, -0.5, 0.0, 0.5, 0.9}) {
    const auto result = external::acos_lookup_table(table, x);
    ASSERT_NEAR(std::acos(x), result.f, 1.0e-4);

    const auto df = wf::numerical_derivative(0.001, [&](double dx) { return std::acos(x + dx); });
    ASSERT_NEAR(df, result.df, 1.0e-3);

    const auto ddf = wf::numerical_derivative(0.001, [&](double dx1) {
      return wf::numerical_derivative(0.001, [&](double dx2) { return std::acos(x + dx1 + dx2); });
    });
    ASSERT_NEAR(ddf, result.ddf, 5.0e-2);
  }
}
