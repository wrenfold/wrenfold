struct CustomInvocationResult {
  double f;
  double df;
  double ddf;
};

// Does not handle the endpoints properly...
Eigen::Vector3d sample_acos_lookup_table(const std::vector<double>& table, double x) {
  const double x_normalized = std::clamp((x + 1.0) / 2.0, 0.0, 1.0);
  const double bucket = x_normalized * static_cast<double>(table.size() - 1);
  const std::size_t i0 = static_cast<std::size_t>(std::floor(bucket));
  const std::size_t i1 = i0 + 1;
  assert(i0 >= 1 && i1 < table.size() - 1);

  const double step_size = 2.0 / static_cast<double>(table.size() - 1);

  // interpolate three quantities:
  // f, central difference of f, and second central difference of f.
  // clang-format off
  const Eigen::Vector3d f0{
      table[i0],
      (table[i0 + 1] - table[i0 - 1]) * 0.5 / step_size,
      (table[i0 + 1] - 2 * table[i0] + table[i0 - 1]) / (step_size * step_size)};
  const Eigen::Vector3d f1{
      table[i1],
      (table[i1 + 1] - table[i1 - 1]) * 0.5 / step_size,
      (table[i1 + 1] - 2 * table[i1] + table[i1 - 1]) / (step_size * step_size)};
  // clang-format on

  // For testing, replace with analytical formulas...
#if 0
  return {std::acos(x), -1.0 / std::sqrt(1 - x * x), -x / std::pow(1 - x * x, 1.5)};
#endif

  return f0 + (f1 - f0) * (bucket - std::floor(bucket));
}

CustomInvocationResult acos_lookup_table(const std::vector<double>& table, double x) {
  const auto samples = sample_acos_lookup_table(table, x);
  return {samples[0], samples[1], samples[2]};
}

